#ifndef STDINT_H_
#define STDINT_H_

#ifndef _UINT8
#define _UINT8
typedef unsigned char         uint8;
#endif

#ifndef _UINT16
#define _UINT16
typedef unsigned short       uint16;
#endif

#ifndef _UINT32
#define _UINT32
typedef unsigned int         uint32;
#endif

#ifndef _UINT64
#define _UINT64
typedef unsigned long long   uint64;
#endif


#endif /* STDINT_H_ */
